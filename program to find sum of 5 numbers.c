//PROGRAM FOR SUMMATION OF 5 NUMBERS
#include <stdio.h>
int main()
{
float q,r,s,t,u,sum;
printf("enter the value of the first number =");
scanf("%f",&q);
printf("enter the value of the second number =");
scanf("%f",&r);
printf("enter the value of the third number =");
scanf("%f",&s);
printf("enter the value of the forth number =");
scanf("%f",&t);
printf("enter the value of the fifth number =");
scanf("%f",&u);
sum=q+r+s+t+u;
printf("sum=%f\n",q+r+s+t+u);
return 0;
}

